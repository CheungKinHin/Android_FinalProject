package com.exmaple2.play_task.data;
public class BillsItem {
    private String name;
    private int score;

    public BillsItem(String name, int score) {
        this.name = name;
        this.score = score;
    }

    // Getters
    public String getName() { return name; }
    public int getScore() { return score; }
}
